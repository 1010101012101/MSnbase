##' MSnbase Package Overview
##'
##' MSnbase provides classes, methods and functions
##' for the plotting, manipulation and processing
##' of mass spectrometry data.
##'
##' Important class are \code{MSnExp} (raw data file),
##' \code{MSnQual} (quality data), \code{MSnSet} (expression data)
##' and \code{ReporterIons} (reporter ions for labelled proteomics).
##' 
##' Other classes are \code{Spectrum} and the subclasses {Spectrum1}
##' (for MS spectra) and \code{Spectrum2} (for MSMS spectra),
##' \code{MIAPE} (Minimum Information about Proteomics Experiments)
##' and MSnProcess (for processing information).
##'
##' Introductory information, use cases and details are
##' available from the vignettes, readily accessible with
##' \code{openVignette()}.
##'
##' Complete listing of available documentation with
##' \code{library(help="MSnbase")}.
##' 
